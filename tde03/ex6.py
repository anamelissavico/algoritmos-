n1=int(input('Digite um número\n'))
n2=int(input('Digite outro número\n'))
n3=int(input('Digite mais um'))
multi= n1*n2*n3
soma=n1+n2+n3
sub= (n1-n2)-n3
resultado=multi+soma+sub
print(f'A multiplicação destes número é {multi}, a soma deles é {soma}, a subtração deles é {sub}. E por fim a soma de todas estas operações é {resultado}')
